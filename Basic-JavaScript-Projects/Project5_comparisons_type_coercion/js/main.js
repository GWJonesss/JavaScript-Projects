//== operator//
var b = 4;
document.write(5==b); // output false because 5 is not equal to b//
document.write("<br>");

// === operator//

var c = 5;
document.write(5===c); // output true because the two operands are equal//
document.write("<br>");

//display > operator//
var d = 4;
document.write(5 > d); // output true because 5 is greater than d//
document.write("<br>");

//display < operator//
var e = 4;
document.write(5 < e); // output false because 5 is not less than e//
document.write("<br>");

//display && operator//
var f = -2;
var g = 5;
document.write(f > 2 && g > 2); // false because both results ar not true//
document.write("<br>");

//display || operator//
var h = 7;
var i = 3;
